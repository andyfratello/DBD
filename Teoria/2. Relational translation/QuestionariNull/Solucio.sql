/* EXERCICI 1 */
SELECT p.NUM_PROJ, SUM(SOU) AS SUM
FROM PROJECTES p LEFT JOIN EMPLEATS e ON p.NUM_PROJ=e.NUM_PROJ  
WHERE p.PRESSUPOST IS NULL 
GROUP BY p.NUM_PROJ
ORDER BY p.NUM_PROJ;

